#include <iostream>
using namespace std;

int main()
{
    int i;

    // For loop
    for (i = 1; i <= 5; i++)
    {
        cout << "mister" << "\n";
    }

    // While loop
    int k;
    k = 1;
    while (k <= 7)
    {
        cout << "ayan " << "\n";
        k++;
    }

    // Do-while loop
    int j = 1;
    do
    {
        cout << "khan" << "\n";
        j++;
    } while (j <= 4);

    return 0;
}
